class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def removeNthFromEnd(self, head: ListNode, n: int) -> ListNode:
        """
        Time Complexity: O(n)
        Space Complexity: O(1)
        """
        sentinel = ListNode(-1, head)
        fast = sentinel
        slow = sentinel
        for _ in range(n):
            fast = fast.next
        while fast.next:
            slow = slow.next
            fast = fast.next
        slow.next = slow.next.next
        return sentinel.next
